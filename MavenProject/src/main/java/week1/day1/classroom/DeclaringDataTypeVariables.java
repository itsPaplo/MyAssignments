package week1.day1.classroom;

public class DeclaringDataTypeVariables {
	
	public static void main(String[] args) {
		int age = 21;
		long phone = 8173737123l;
		float height = 5.8f;
		double fees = 38762;
		char initial = 'C';
		String name = "Phillip";
		boolean isEnrolled = true;
		
		System.out.println("Student details:- ");
		System.out.println("Name of Student: " + initial + " " + name);;
		System.out.println("Age:" + age);
		System.out.println("Height:" + height);
		System.out.println("Phone number: " + phone);
		System.out.println("Enrolled to course: " + isEnrolled);
		System.out.println("Upcoming Fees: " + fees + "rs");
	}

}
