public class OddorEven {
	
	public static void main(String[] args) {
		int a = 4;
		if(a%2==0) {
			System.out.println("Given number " + a +" is an Even number");			
		}
		else {
			System.out.println("Given number " + a + " is an Odd number");
		}
	}

}
